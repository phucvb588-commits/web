let currentLang = localStorage.getItem('sdb_lang') || 'vi';

function switchLang() {
    currentLang = currentLang === 'vi' ? 'en' : 'vi';
    localStorage.setItem('sdb_lang', currentLang);
    window.location.reload();
}

function applyLang() {
    const dict = i18nConfig[currentLang];
    
    document.title = dict.title;
    document.documentElement.lang = currentLang;

    // Apply translations to innerHTML
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) el.innerHTML = dict[key];
    });

    // Apply translations to placeholders
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
        const key = el.getAttribute('data-i18n-ph');
        if (dict[key]) el.placeholder = dict[key];
    });
    
    // Update Toggle Button Icon Text
    const langIcon = document.getElementById('langIcon');
    if (langIcon) {
        langIcon.textContent = currentLang === 'vi' ? 'EN' : 'VI';
    }

    // Trigger an event so script.js knows language has been changed 
    // to update dynamic components (like categories inside js)
    window.dispatchEvent(new CustomEvent('langChanged', { detail: { lang: currentLang } }));
}

document.addEventListener('DOMContentLoaded', () => {
    applyLang();
    const btn = document.getElementById('langToggleBtn');
    if (btn) btn.addEventListener('click', switchLang);
});

